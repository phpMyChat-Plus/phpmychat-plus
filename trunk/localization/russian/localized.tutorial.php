<FONT SIZE="+1"><A NAME="ignore"><B>Игнорирование пользователей:</B></A></FONT>
<P>
Для игнорирования всех сообщений от пользователя, напишите <B>команда "/ignore имя_пользователя"</B> без кавычек.
<P>
<I>Например:</I> /ignore Jack
<P>
С этого момента никакие сообщения от пользователя Jack небудут отображаться на вашем экране.
<P>
Чтобы посмотреть список пользователей которых вы игнорируете, просто напишите <B>команду "/ignore"</B> без кавычек.
<P>
Если вы захотите видеть сообщения от игнорируемых пользователей, напишите <B>команда "/ignore - имя_пользователя"</B> без кавычек, где "-" это дефис (знак минуса).<P>
<P>
<I>Например:</I> /ignore - Jack
<P>
Теперь все сообщения от Jack отправленные в течении текущего сеанса чата, будут отображаться на вашем экране, включая те сообщения, которые Jack прежде, чем вы напечатали эту команду.<br />
Если вы не укажите имя пользователя после дефиса, ваш игнор лист будет очищен.
<P>
Обратите внимание, что вы можете вставить больше чем одно имя пользователя в игнор команду (например "/ignore Jack,Helen,Alf" или "/ignore - Jack,Alf"). Они должны быть разделены запятой (,) без пробелов.
